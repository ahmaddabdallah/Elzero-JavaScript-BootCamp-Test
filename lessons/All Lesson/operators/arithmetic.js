/*
Arithmetic Operators
  + Addition
  - Subtraction
  × Multiplication
  / Division
  ** Exponentiation (ES7)
  % Modulus (Division Remainder)
  ++ Increment [ Post / Pre ]
  -- Decrement [ Post / Pre ]
*/

// addition
console.log(10 + 20); //30
console.log(10 + 'Ahmad'); //10Ahmad => concatenate

// subtraction
console.log(10 - 20);
console.log(10 - 'Ahmad'); // NaN
console.log(typeof 10 - 'Ahmad'); // NaN

console.log(typeof NaN); // NaN => Number
console.log(typeof null); // Null => Objects

// multiplication
console.log(10 * 20); // 200

// Division
console.log(20 / 5); // 4
console.log(21 / 8); // 2.625

// Exponentiation

console.log(10 ** 2); // 100
console.log(50 ** 2); // 2500

// Modulus
console.log(10 % 2); // 0
console.log(11 % 2); // 1
