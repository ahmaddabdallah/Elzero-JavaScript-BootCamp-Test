    console.log("Ahmad Abdallah")
    console.log(typeof "Ahmad Abdallah")

    console.log(5000)
    console.log(typeof 5000)

    console.log(5000.64654)
    console.log(typeof 5000.65464)

    console.log([1 , 34,5,3,5,7,42,2,2])
    console.log(typeof [1 , 34,5,3,5,7,42,2,2])

    console.log({name: "Ahmad", age: 18, country: "Egypt"})
    console.log(typeof {name: "Ahmad", age: 18, country: "Egypt"})

    console.log(typeof true)
    console.log(false)

    console.log(null)
    console.log(undefined)

